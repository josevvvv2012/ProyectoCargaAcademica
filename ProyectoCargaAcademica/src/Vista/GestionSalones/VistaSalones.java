    /*
    @cargar las datos de la tabla salones
    @autor jose vanegas . jvanegasv@ucentral.edu.co
    */    
    public void cargar() {

        DefaultTableModel tabla = new DefaultTableModel();
        PreparedStatement ps;
        try {
            tabla.addColumn("Cod Salon");
            tabla.addColumn("Nombre");
            tabla.addColumn("Ubicacion");
            tabla.addColumn("Estado");
            
            ps = cn.prepareStatement("SELECT codigoSalon, nombreSalon, ubicacion ,estado FROM salon");
            r = ps.executeQuery();

            while (r.next()) {
                Object dato[] = new Object[4];
                
                for (int i = 0; i < 4; i++) {
                    dato[i] = r.getString(i + 1);
                    //log(String.valueOf(dato[0]));
                }
                tabla.addRow(dato);
            }
            this.jTable1.setModel(tabla);

        } catch (Exception e) {
        }
    }
    
