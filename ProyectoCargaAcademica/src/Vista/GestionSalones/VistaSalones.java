    /*
     @eliminar salon
     @autor jose vanegas jvanegasv@ucentral.edu.co
     */
    public void eliminarSalon(int idSalon) {

        // TODO add your handling code here:
        String cod = Integer.toString(idSalon);
        //     log("idUsuario"+String.valueOf(cod));
        try {
            cts = cn.prepareCall("{call eliminarSalon(?)}");
            cts.setString(1, cod);
            int rpta = cts.executeUpdate();

            if (rpta == 1) {
                JOptionPane.showMessageDialog(this, "Salon Eliminado Correctamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);

            } else {
                JOptionPane.showMessageDialog(this, "Empleado No Eliminado", "Aviso", JOptionPane.INFORMATION_MESSAGE);

            }

        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    } 
    
