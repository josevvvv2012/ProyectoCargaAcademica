   /*
     *Seleccionar los datos de la tabla salon para editar
    @autor jose vanegas -jvanegasv@ucentral.edu.co
    */
 public int seleccionarfila(int a) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        //ahora obtenemos la fila selccionada
        int fila_select = jTable1.getSelectedRow();

        if (fila_select < 0) {
            // no se puede eliminar
            JOptionPane.showMessageDialog(this, "Tabla vacia o no ha seleccionado uan fila para editar.");
        } else {
          
          formSalones = new  CUPSalones();
          formSalones.setVisible(true);
          
          
          String codSalon =jTable1.getValueAt(row, 0).toString();
          String nombre =jTable1.getValueAt(row,1).toString();
          String ubicacion =jTable1.getValueAt(row, 2).toString();
          String Estado =jTable1.getValueAt(row, 3).toString();
          
          formSalones.btnGuardar.setText(getMod());
          formSalones.txtCodigo.setEnabled(false);
        
         formSalones.txtCodigo.setText(codSalon);
         formSalones.txtNombreS.setText(nombre);
         formSalones.txtSalonU.setText(ubicacion);
        formSalones.comboEstadoS.setSelectedIndex(1);
         
            a = 1;
            
        }
        
        
        return a;
    }
    
